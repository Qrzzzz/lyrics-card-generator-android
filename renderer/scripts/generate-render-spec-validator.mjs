import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const schema = JSON.parse(await readFile(resolve(root, "schema/render-spec-v1.schema.json"), "utf8"));
const ajv = new Ajv2020({
  allErrors: true,
  strict: true,
  strictTypes: false,
  code: { source: true, esm: true }
});
const validate = ajv.compile(schema);
const generated = standaloneCode(ajv, validate).replace(
  'const func2 = require("ajv/dist/runtime/ucs2length").default;',
  "const func2 = (value) => Array.from(value).length;"
);
const output = [
  "// Generated by scripts/generate-render-spec-validator.mjs. Do not edit by hand.",
  "// @ts-nocheck",
  generated,
  ""
].join("\n");
const outputPath = resolve(root, "src/generated/renderSpecValidator.ts");

await mkdir(dirname(outputPath), { recursive: true });
let current = "";
try {
  current = await readFile(outputPath, "utf8");
} catch {
  // The first build creates the generated module.
}
if (current !== output) {
  await writeFile(outputPath, output, "utf8");
}
